package com.maclaren.bank.util.bean;

public class Unit2Unit 
{
	private String id;
	
	private String unit1_id;
	
	private String unit2_id;
	
	private float rate;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUnit1_id() {
		return unit1_id;
	}

	public void setUnit1_id(String unit1Id) {
		unit1_id = unit1Id;
	}

	public String getUnit2_id() {
		return unit2_id;
	}

	public void setUnit2_id(String unit2Id) {
		unit2_id = unit2Id;
	}

	public float getRate() {
		return rate;
	}

	public void setRate(float rate) {
		this.rate = rate;
	}
}
